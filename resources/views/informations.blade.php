<h4>Des enjeux sanitaires liés à la qualité de l’air</h4>

<p>La pollution de l’air représente un enjeu de santé publique. En effet, de nombreuses études épidémiologiques ont établi l’existence d’effets sanitaires de la pollution atmosphérique sur la mortalité et la morbidité. Deux types d’effets ont pu être mis en évidence : des effets à court-terme, qui surviennent dans les heures, jours ou semaines suivant l’exposition et se manifestent selon la vulnérabilité des personnes (nourrissons et enfants de moins de 5 ans, femmes enceintes, personnes âgées, personnes souffrant de pathologies chroniques, fumeurs, etc.) par des effets bénins (toux, hypersécrétion nasale, expectoration, essoufflement, irritation nasale des yeux et de la gorge, etc.) ou plus graves (recours aux soins pour causes cardiovasculaires ou respiratoires voire décès) et des effets à long-terme qui résultent d’une exposition répétée ou continue tout au long de la vie à des niveaux inférieurs aux seuils d’information et d’alerte réglementaires.</br></p>

