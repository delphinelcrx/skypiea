<h4>Définition</h4>

<p>La pollution de l’air (ou pollution atmosphérique) est une modification de la composition de l’air par des polluants nuisibles à la santé et à l’environnement. Elle est parfois caractérisée par des mesures de polluants gazeux, particulaires, et peut-être liquides présents dans l’air.</br>

La pollution de l’atmosphère peut ainsi être vue comme une présence dans l’air, d’un ajout d’agents chimiques, biologiques ou physiques ayant des conséquences préjudiciables à la santé humaine, aux êtres vivants, au climat, ou aux biens matériels.</br>
Ces polluants proviennent des activités humaines ou de la nature.</p>

